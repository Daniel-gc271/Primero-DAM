    private void IgnicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IgnicionActionPerformed
        // TODO add your handling code here:
        boolean estadoIgnicion = moto.isArrancado();
        if (estadoIgnicion == false) {
            Ignicion.setText("Parar");

            estadoIgnicion = true;
            System.out.println("Arrancar");
            moto.arrancar();
        } else {
            Ignicion.setText("Arrancar");
            estadoIgnicion = false;
            System.out.println("parando");
            moto.parar();

        }
    }//GEN-LAST:event_IgnicionActionPerformed


