    private void datosMoto() {
        this.setTitle(moto.getMatricula() + "-"
                + moto.getMarca() + " " + moto.getModelo());
    }

    private void mostrarIndicadores() {
        this.Velocímetro.setText(String.valueOf(moto.getVelocidad()));
        this.IndicadorCombustible.setText(String.valueOf(moto.getDeposito()));
        this.IndicadorOcupantes.setText(String.valueOf(moto.getPasajeros()));
        if (moto.isArrancado()) {
            this.IndicadorIgnicion.setBackground(Color.GREEN);
        } else {
            this.IndicadorIgnicion.setBackground(Color.RED);
        }
    }

