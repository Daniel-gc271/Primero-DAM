    private  int contNombComp() {
    int cont=0;
        for (String listaNombre : listaNombres) {
            if(listaNombre.contains(" ")) {cont+=1;}
        }
    return cont;
    }
    private  int contNombInic(char c) {
    int cont=0;
        for (String listaNombre : listaNombres) {
            if(listaNombre.charAt(0)== c) {cont+=1;}
        }
    return cont;
    }
