    private void ViewStatsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewStatsActionPerformed
        // TODO add your handling code here:
        recuentoVotos();
        StringBuilder msg = new StringBuilder(formatDateTimeStats());
        if (numVotos != 0) {
            msg.append(formatVoteStats());
        }
        JOptionPane.showConfirmDialog(this, msg, "Estadisticas", 0);
    }//GEN-LAST:event_ViewStatsActionPerformed
    private void recuentoVotos() {
        this.favorables = 0;
        this.contrarios = 0;
        this.numVotos = listaVotaciones.size();
        for (Respuesta voto : listaVotaciones) {
            if (voto == Respuesta.SI) {
                this.favorables++;
            } else {
                this.contrarios++;
            };
        }
    }

    private String formatDateTimeStats() {
        StringBuilder statsFormateados = new StringBuilder();
        Date fechaActual = new Date();
        SimpleDateFormat formateadorFecha = new SimpleDateFormat("dd' de 'MM' de 'yyyy");
        SimpleDateFormat formateadorHora = new SimpleDateFormat("HH':'mm':'ss");
        statsFormateados.append("Estamos a: ").append(formateadorFecha.format(fechaActual)).append('\n');
        statsFormateados.append("Son las: ").append(formateadorHora.format(fechaActual)).append('\n');
        statsFormateados.append("Hay un total de: ").append(numVotos).append(" votos\n");
        return statsFormateados.toString();

    }

    private String formatVoteStats() {
        StringBuilder statsFormateados = new StringBuilder();
        statsFormateados.append(favorables).append(" son a favor\n");
        statsFormateados.append(contrarios).append(" son en contra\n");
        return statsFormateados.toString();
    }

