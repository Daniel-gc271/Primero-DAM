    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        String nombreDoc = this.jTextField1.getText();
        int numPag = (int) this.jSpinner1.getValue();
        if (nombreDoc == null || nombreDoc.isBlank()) {
            JOptionPane.showMessageDialog(this, "Debes de dar un nombre de archivo",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Documento doc = new Documento(nombreDoc, numPag);
        //meto en la cola de impresión
        modeloLista.add(doc);
        //y en la lista
//        modeloLista.addElement(doc);
        //estado de la cola
        //System.out.println("Cola de impresión: " + colaImpresion);
    }//GEN-LAST:event_jButtonAgregarActionPerformed

