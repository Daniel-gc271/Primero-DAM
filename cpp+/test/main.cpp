const unsigned char rcon[11] = { 0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

// --- MATEM√ÅTICAS GF(2^8) ---
unsigned char xtime(unsigned char x) { return (x << 1) ^ (((x >> 7) & 1) * 0x1b); }
unsigned char multiply(unsigned char x, unsigned char y) {
    unsigned char res = 0;
    for (int i = 0; i < 8; i++) {
        if ((y >> i) & 1) res ^= x;
        x = xtime(x);
    }
    return res;
}

// --- CORE AES ---
void key_expansion(const unsigned char* key, unsigned char* round_keys) {
    for (int i = 0; i < 16; i++) round_keys[i] = key[i];
    int bytes = 16, rcon_idx = 1;
    unsigned char t[4];
    while (bytes < 176) {
        for (int i = 0; i < 4; i++) t[i] = round_keys[bytes - 4 + i];
        if (bytes % 16 == 0) {
            unsigned char rot = t[0];
            t[0] = sbox[t[1]] ^ rcon[rcon_idx++];
            t[1] = sbox[t[2]]; t[2] = sbox[t[3]]; t[3] = sbox[rot];
        }
        for (int i = 0; i < 4; i++) { round_keys[bytes] = round_keys[bytes - 16] ^ t[i]; bytes++; }
    }
}

void mix_columns(unsigned char* s) {
    for (int i = 0; i < 4; i++) {
        unsigned char a = s[i*4], b = s[i*4+1], c = s[i*4+2], d = s[i*4+3];
        s[i*4]   = xtime(a) ^ (xtime(b) ^ b) ^ c ^ d;
        s[i*4+1] = a ^ xtime(b) ^ (xtime(c) ^ c) ^ d;
        s[i*4+2] = a ^ b ^ xtime(c) ^ (xtime(d) ^ d);
        s[i*4+3] = (xtime(a) ^ a) ^ b ^ c ^ xtime(d);
    }
}

void inv_mix_columns(unsigned char* s) {
    for (int i = 0; i < 4; i++) {
        unsigned char a = s[i*4], b = s[i*4+1], c = s[i*4+2], d = s[i*4+3];
        s[i*4]   = multiply(a, 0x0e) ^ multiply(b, 0x0b) ^ multiply(c, 0x0d) ^ multiply(d, 0x09);
        s[i*4+1] = multiply(a, 0x09) ^ multiply(b, 0x0e) ^ multiply(c, 0x0b) ^ multiply(d, 0x0d);
        s[i*4+2] = multiply(a, 0x0d) ^ multiply(b, 0x09) ^ multiply(c, 0x0e) ^ multiply(d, 0x0b);
        s[i*4+3] = multiply(a, 0x0b) ^ multiply(b, 0x0d) ^ multiply(c, 0x09) ^ multiply(d, 0x0e);
    }
}

void encrypt_block(unsigned char* s, const unsigned char* rk) {
    for (int i = 0; i < 16; i++) s[i] ^= rk[i];
    for (int r = 1; r <= 10; r++) {
        for (int i = 0; i < 16; i++) s[i] = sbox[s[i]];
        unsigned char t[16];
        int sh[] = {0,5,10,15,4,9,14,3,8,13,2,7,12,1,6,11};
        for(int i=0; i<16; i++) t[i] = s[sh[i]];
        for(int i=0; i<16; i++) s[i] = t[i];
        if (r < 10) mix_columns(s);
        for (int i = 0; i < 16; i++) s[i] ^= rk[r * 16 + i];
    }
}

void decrypt_block(unsigned char* s, const unsigned char* rk) {
    for (int i = 0; i < 16; i++) s[i] ^= rk[160 + i];
    for (int r = 9; r >= 0; r--) {
        unsigned char t[16];
        int sh[] = {0,13,10,7,4,1,14,11,8,5,2,15,12,9,6,3};
        for(int i=0; i<16; i++) t[i] = s[sh[i]];
        for(int i=0; i<16; i++) s[i] = t[i];
        for (int i = 0; i < 16; i++) s[i] = rsbox[s[i]];
        for (int i = 0; i < 16; i++) s[i] ^= rk[r * 16 + i];
        if (r > 0) inv_mix_columns(s);
    }
}

